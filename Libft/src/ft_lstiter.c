#include "libft.h"

void	ft_lstiter(t_list *lst, void (*f)(t_list *elem))
{
	if (lst)
	{
		f(lst);
		while (lst->next != NULL)
		{
			lst = lst->next;
			f(lst);
		}
	}
}
