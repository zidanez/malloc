#include "libft.h"

char	*ft_strcat(char *restrict s1, const char *restrict s2)
{
	char	*s1_beg;

	s1_beg = s1;
	while (*s1)
		s1++;
	while (*s2)
	{
		*s1 = *s2;
		s1++;
		s2++;
	}
	*s1 = '\0';
	return (s1_beg);
}
